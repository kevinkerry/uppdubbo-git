package com.csii.upp.qrcodeplatform.action.constant;

public class AlipayParameter {
	
	/**
	 * 支付宝回调的通知地址
	 */
	private String notifyUrl;
	
	/**
	 * 支付宝应用id
	 */
	private String appid;
	
	/**
	 * 支付宝商户号
	 */
	private String mch_id;
	
	/**
	 * 商户私钥
	 */
	private String merPrivateKey;
	
	/**
	 * 支付宝公钥
	 */
	private String publicKey;
	
	/**
	 * 编码格式 
	 */
	private String charset;
	
	/**
	 * 支付宝所有交易通用网关
	 */
	private String url;
	
	/**
	 * 支付宝查询最大重试次数
	 */
	private String maxQueryRetry;
	
	/**
	 * 支付宝查询间隔
	 */
	private String queryDuration;

	public String getNotifyUrl() {
		return notifyUrl;
	}

	public void setNotifyUrl(String notifyUrl) {
		this.notifyUrl = notifyUrl;
	}

	public String getAppid() {
		return appid;
	}

	public void setAppid(String appid) {
		this.appid = appid;
	}

	public String getMch_id() {
		return mch_id;
	}

	public void setMch_id(String mch_id) {
		this.mch_id = mch_id;
	}

	public String getMerPrivateKey() {
		return merPrivateKey;
	}

	public void setMerPrivateKey(String merPrivateKey) {
		this.merPrivateKey = merPrivateKey;
	}

	public String getPublicKey() {
		return publicKey;
	}

	public void setPublicKey(String publicKey) {
		this.publicKey = publicKey;
	}

	public String getCharset() {
		return charset;
	}

	public void setCharset(String charset) {
		this.charset = charset;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getMaxQueryRetry() {
		return maxQueryRetry;
	}

	public void setMaxQueryRetry(String maxQueryRetry) {
		this.maxQueryRetry = maxQueryRetry;
	}

	public String getQueryDuration() {
		return queryDuration;
	}

	public void setQueryDuration(String queryDuration) {
		this.queryDuration = queryDuration;
	}
	
	
	
}
