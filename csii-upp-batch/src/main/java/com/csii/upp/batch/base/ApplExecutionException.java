package com.csii.upp.batch.base;

public class ApplExecutionException extends Exception {
	private static final long serialVersionUID = -8349320767236763995L;
	private ApplConfiguration applConfig;

	public ApplExecutionException(ApplConfiguration applConfig) {
		super();
		this.applConfig = applConfig;
	}
	
	public ApplExecutionException(ApplConfiguration applConfig,Throwable cause){
		super(cause);
		this.applConfig = applConfig;
	}
}
