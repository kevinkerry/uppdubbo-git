/**
 * 大额系统处理结果查询输出参数
 * 
 * @author 陈彦鹏
 *
 */
public class RespHvpsQueryRtxn extends RespHvpsHead {
	private String refno         ;  //原支付流水号
	private String channelno     ;  //原渠道流水号
	private String trandate      ;  //原交易日期
	private String branchid      ;  //原渠道机构号
	private String settledate   ;  //清算日期
	private String busistatus;  //交易状态
	private String acctstatus;  //账户处理状态
	private String responsecode;  //处理码
	
	
	public String getRefno() {
		return refno;
	}
	public void setRefno(String refno) {
		this.refno = refno;
	}
	public String getChannelno() {
		return channelno;
	}
	public void setChannelno(String channelno) {
		this.channelno = channelno;
	}
	public String getTrandate() {
		return trandate;
	}
	public void setTrandate(String trandate) {
		this.trandate = trandate;
	}
	public String getBranchid() {
		return branchid;
	}
	public void setBranchid(String branchid) {
		this.branchid = branchid;
	}
	public String getSettledate() {
		return settledate;
	}
	public void setSettledate(String settledate) {
		this.settledate = settledate;
	}
	public String getBusistatus() {
		return busistatus;
	}
	public void setBusistatus(String busistatus) {
		this.busistatus = busistatus;
	}
	public String getAcctstatus() {
		return acctstatus;
	}
	public void setAcctstatus(String acctstatus) {
		this.acctstatus = acctstatus;
	}
	public String getResponsecode() {
		return responsecode;
	}
	public void setResponsecode(String responsecode) {
		this.responsecode = responsecode;
	}
	public String getResponsemessage() {
		return responsemessage;
	}
	public void setResponsemessage(String responsemessage) {
		this.responsemessage = responsemessage;
	}
	private String responsemessage;  //处理消息

}
