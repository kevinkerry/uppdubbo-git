package com.csii.upp.dto.router.eaccount;

import java.util.Date;

import com.csii.upp.idto.IDto;

public class BaseRequest implements IDto {
	/**
	 * 渠道编号
	 */
	private String sourceId;
	/**
	 * 渠道工作日期
	 */
	private Date workDate;
	/**
	 * 渠道流水号
	 */
	private String seqNo;
	/**
	 * 对账标识号
	 */
	private String batchNbr;
	/**
	 * 授权参考号
	 */
	private String apprRefId;
	/**
	 * 交易时间戳
	 */
	private Date tranTime;
	/**
	 * 接入设备MAC地址
	 */
	private String macValue;
	/**
	 * 柜员号
	 */
	private String tellerNbr;
	
	private String branchNbr;// 机构号
	private String bankOrgNbr;// 法人号

	public String getSourceId() {
		return sourceId;
	}

	public void setSourceId(String sourceId) {
		this.sourceId = sourceId;
	}

	public Date getWorkDate() {
		return workDate;
	}

	public void setWorkDate(Date workDate) {
		this.workDate = workDate;
	}

	public String getSeqNo() {
		return seqNo;
	}

	public void setSeqNo(String seqNo) {
		this.seqNo = seqNo;
	}

	public String getBatchNbr() {
		return batchNbr;
	}

	public void setBatchNbr(String batchNbr) {
		this.batchNbr = batchNbr;
	}

	public String getApprRefId() {
		return apprRefId;
	}

	public void setApprRefId(String apprRefId) {
		this.apprRefId = apprRefId;
	}

	public Date getTranTime() {
		return tranTime;
	}

	public void setTranTime(Date tranTime) {
		this.tranTime = tranTime;
	}

	public String getMacValue() {
		return macValue;
	}

	public void setMacValue(String macValue) {
		this.macValue = macValue;
	}

	public String getTellerNbr() {
		return tellerNbr;
	}

	public String getBranchNbr() {
		return branchNbr;
	}

	public String getBankOrgNbr() {
		return bankOrgNbr;
	}

	public void setBranchNbr(String branchNbr) {
		this.branchNbr = branchNbr;
	}

	public void setBankOrgNbr(String bankOrgNbr) {
		this.bankOrgNbr = bankOrgNbr;
	}

	public void setTellerNbr(String tellerNbr) {
		this.tellerNbr = tellerNbr;
	}

}
