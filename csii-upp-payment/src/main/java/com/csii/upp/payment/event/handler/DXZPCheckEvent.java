package com.csii.upp.payment.event.handler;

import java.math.BigDecimal;
import java.util.Date;

import com.csii.upp.event.Event;
import com.csii.upp.service.payment.NotifyService;

public class DXZPCheckEvent extends Event {
	
	private boolean scheduleNotify;
	private NotifyService notifyService;
	private Date transTime;
	private Object trsferONum;
	private Object trsferOName;
	private Object trsferOBank;
	private Object trsferINum;
	private Object trsferIName;
	private Object trsferIBank;
	private BigDecimal transAmount;
	private Object cuurrency;
	
	
	
	public boolean isScheduleNotify() {
		return scheduleNotify;
	}
	public void setScheduleNotify(boolean scheduleNotify) {
		this.scheduleNotify = scheduleNotify;
	}
	
	public Date getTransTime() {
		return transTime;
	}
	public void setTransTime(Date transTime) {
		this.transTime = transTime;
	}
	public Object getTrsferONum() {
		return trsferONum;
	}
	public void setTrsferONum(Object trsferONum) {
		this.trsferONum = trsferONum;
	}
	public Object getTrsferOName() {
		return trsferOName;
	}
	public void setTrsferOName(Object trsferOName) {
		this.trsferOName = trsferOName;
	}
	public Object getTrsferOBank() {
		return trsferOBank;
	}
	public void setTrsferOBank(Object trsferOBank) {
		this.trsferOBank = trsferOBank;
	}
	public Object getTrsferINum() {
		return trsferINum;
	}
	public void setTrsferINum(Object trsferINum) {
		this.trsferINum = trsferINum;
	}
	public Object getTrsferIName() {
		return trsferIName;
	}
	public void setTrsferIName(Object trsferIName) {
		this.trsferIName = trsferIName;
	}
	public Object getTrsferIBank() {
		return trsferIBank;
	}
	public void setTrsferIBank(Object trsferIBank) {
		this.trsferIBank = trsferIBank;
	}
	public BigDecimal getTransAmount() {
		return transAmount;
	}
	public void setTransAmount(BigDecimal transAmount) {
		this.transAmount = transAmount;
	}
	public Object getCuurrency() {
		return cuurrency;
	}
	public void setCuurrency(Object cuurrency) {
		this.cuurrency = cuurrency;
	}
	public NotifyService getNotifyService() {
		return notifyService;
	}
	public void setNotifyService(NotifyService notifyService) {
		this.notifyService = notifyService;
	}
}
