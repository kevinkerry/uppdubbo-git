package com.csii.upp.custom.common.api.data.base;

import java.io.Serializable;

import javax.validation.constraints.NotNull;

public abstract class BaseInput implements Serializable {
	private static final long serialVersionUID = -4431752079642876845L;
	@NotNull
	private String servcId;
	@NotNull
	private String chnlId;
	private String termnId;
	private String branchId;
	private String operId;
	@NotNull
	private String reqTime;
	@NotNull
	private String bizTrackNo;
	@NotNull
	private String reqSeqNo;
	private String clientIp;
	private String summery;
	public String getServcId() {
		return servcId;
	}
	public void setServcId(String servcId) {
		this.servcId = servcId;
	}
	public String getChnlId() {
		return chnlId;
	}
	public void setChnlId(String chnlId) {
		this.chnlId = chnlId;
	}
	public String getTermnId() {
		return termnId;
	}
	public void setTermnId(String termnId) {
		this.termnId = termnId;
	}
	public String getBranchId() {
		return branchId;
	}
	public void setBranchId(String branchId) {
		this.branchId = branchId;
	}
	public String getOperId() {
		return operId;
	}
	public void setOperId(String operId) {
		this.operId = operId;
	}
	public String getReqTime() {
		return reqTime;
	}
	public void setReqTime(String reqTime) {
		this.reqTime = reqTime;
	}
	public String getBizTrackNo() {
		return bizTrackNo;
	}
	public void setBizTrackNo(String bizTrackNo) {
		this.bizTrackNo = bizTrackNo;
	}
	public String getReqSeqNo() {
		return reqSeqNo;
	}
	public void setReqSeqNo(String reqSeqNo) {
		this.reqSeqNo = reqSeqNo;
	}
	public String getClientIp() {
		return clientIp;
	}
	public void setClientIp(String clientIp) {
		this.clientIp = clientIp;
	}
	public String getSummery() {
		return summery;
	}
	public void setSummery(String summery) {
		this.summery = summery;
	}
	
	@Override
	public String toString() {
		return "BaseInput [servcId=" + servcId + ", chnlId=" + chnlId + ", termnId=" + termnId + ", branchId="
				+ branchId + ", operId=" + operId + ", reqTime=" + reqTime + ", bizTrackNo=" + bizTrackNo
				+ ", reqSeqNo=" + reqSeqNo + ", clientIp=" + clientIp + ", summery=" + summery + ", toString()="
				+ super.toString() + "]";
	}
	
	

}